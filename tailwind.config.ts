import type { Config } from "tailwindcss";
import type { PluginAPI } from "tailwindcss/types/config"; // Import the correct type

const config: Config = {
  content: [
    "./pages/**/*.{js,ts,jsx,tsx,mdx}",
    "./components/**/*.{js,ts,jsx,tsx,mdx}",
    "./app/**/*.{js,ts,jsx,tsx,mdx}",
  ],
  theme: {
    extend: {
      backgroundImage: {
        "gradient-radial": "radial-gradient(var(--tw-gradient-stops))",
        "gradient-conic":
          "conic-gradient(from 180deg at 50% 50%, var(--tw-gradient-stops))",
      },
      animation: {
        aurora: "aurora 60s linear infinite", // Aurora animation
      },
      keyframes: {
        aurora: {
          from: {
            backgroundPosition: "50% 50%, 50% 50%",
          },
          to: {
            backgroundPosition: "350% 50%, 350% 50%",
          },
        },
      },
    },
  },
  plugins: [
    function addVariablesForColors(api: PluginAPI) {
      const allColors = api.theme("colors");
      if (allColors) {
        const flattenedColors = Object.entries(allColors).reduce((acc, [key, value]) => {
          acc[`--${key}`] = value;
          return acc;
        }, {} as { [key: string]: string });

        api.addBase({
          ":root": flattenedColors,
        });
      }
    },
  ],
};

export default config;

